package com.cc.setup.property;

public class PropertyCollection {
	DbPropertyArray dbPropertyArray;
	FilePropertyArray filePropertyArray;
	UrlsArray urlsArray;
	ScriptPropertyArray scriptPropertyArray;
	AlertPropertyArray alertPropertyArray;
	OtherArray otherArray;
	public DbPropertyArray getDbPropertyArray() {
		return dbPropertyArray;
	}
	public void setDbPropertyArray(DbPropertyArray dbPropertyArray) {
		this.dbPropertyArray = dbPropertyArray;
	}
	public FilePropertyArray getFilePropertyArray() {
		return filePropertyArray;
	}
	public void setFilePropertyArray(FilePropertyArray filePropertyArray) {
		this.filePropertyArray = filePropertyArray;
	}
	public UrlsArray getUrlsArray() {
		return urlsArray;
	}
	public void setUrlsArray(UrlsArray urlsArray) {
		this.urlsArray = urlsArray;
	}
	public ScriptPropertyArray getScriptPropertyArray() {
		return scriptPropertyArray;
	}
	public void setScriptPropertyArray(ScriptPropertyArray scriptPropertyArray) {
		this.scriptPropertyArray = scriptPropertyArray;
	}
	public AlertPropertyArray getAlertPropertyArray() {
		return alertPropertyArray;
	}
	public void setAlertPropertyArray(AlertPropertyArray alertPropertyArray) {
		this.alertPropertyArray = alertPropertyArray;
	}
	public OtherArray getOtherArray() {
		return otherArray;
	}
	public void setOtherArray(OtherArray otherArray) {
		this.otherArray = otherArray;
	}
	
}
