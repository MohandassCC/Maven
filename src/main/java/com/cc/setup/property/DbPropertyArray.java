package com.cc.setup.property;

public class DbPropertyArray {
	private String applicationDb;
	private String jdbcdriver;
	private String jdbcurl;
	private String username;
	private String password;
	private String port;
	private String dbinstance;
	private String sparkjdbcUrl;
	
	public String getApplicationDb() {
		return applicationDb;
	}
	public void setApplicationDb(String applicationDb) {
		this.applicationDb = applicationDb;
	}
	public String getJdbcdriver() {
		return jdbcdriver;
	}
	public void setJdbcdriver(String jdbcdriver) {
		this.jdbcdriver = jdbcdriver;
	}
	public String getJdbcurl() {
		return jdbcurl;
	}
	public void setJdbcurl(String jdbcurl) {
		this.jdbcurl = jdbcurl;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getPort() {
		return port;
	}
	public void setPort(String port) {
		this.port = port;
	}
	public String getDbinstance() {
		return dbinstance;
	}
	public void setDbinstance(String dbinstance) {
		this.dbinstance = dbinstance;
	}
	public String getSparkjdbcUrl() {
		return sparkjdbcUrl;
	}
	public void setSparkjdbcUrl(String sparkjdbcUrl) {
		this.sparkjdbcUrl = sparkjdbcUrl;
	}	
}
