package com.cc.setup.property;

public class ScriptPropertyArray {
	private String pythonfilePath;
	private String rfilePath;
	private String rFile;
	private String javaFilePath;
	
	public String getPythonfilePath() {
		return pythonfilePath;
	}
	public void setPythonfilePath(String pythonfilePath) {
		this.pythonfilePath = pythonfilePath;
	}
	public String getRfilePath() {
		return rfilePath;
	}
	public void setRfilePath(String rfilePath) {
		this.rfilePath = rfilePath;
	}
	public String getrFile() {
		return rFile;
	}
	public void setrFile(String rFile) {
		this.rFile = rFile;
	}
	public String getJavaFilePath() {
		return javaFilePath;
	}
	public void setJavaFilePath(String javaFilePath) {
		this.javaFilePath = javaFilePath;
	}		
}
